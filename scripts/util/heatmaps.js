function flipHeat(heat) {
	var flipped;
	for (i = 0; i < heat.length; i++) {
		flipped[i] = new Array(heat.length);
		for (j = 0; j < heat[i].length; j++) {
			flipped[i][j] = heat[heat.length - i][heat[i].length - j];
		}
	}
	return flipped;
}
